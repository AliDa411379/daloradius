-- ========================================================
-- Balance Reconciliation & Deep Scan Queries
-- Verifies money_balance matches topup payments & deductions
-- ========================================================


-- --------------------------------------------------------
-- QUERY 1: Global summary - system-wide numbers
-- --------------------------------------------------------
SELECT
    COUNT(DISTINCT ubi.id) AS total_users_with_balance,
    SUM(ubi.money_balance) AS sum_all_balances,
    (SELECT SUM(amount) FROM agent_payments WHERE payment_type = 'balance_topup') AS sum_all_topups,
    (SELECT SUM(purchase_amount) FROM user_bundles WHERE purchase_amount > 0) AS sum_all_bundle_purchases,
    (SELECT COUNT(*) FROM agent_payments WHERE payment_type = 'balance_topup') AS total_topup_count,
    (SELECT COUNT(*) FROM user_bundles WHERE purchase_amount > 0) AS total_bundle_count,
    ROUND(
        (SELECT SUM(amount) FROM agent_payments WHERE payment_type = 'balance_topup') -
        (SELECT SUM(purchase_amount) FROM user_bundles WHERE purchase_amount > 0),
        2
    ) AS expected_total_balance,
    ROUND(
        (
            (SELECT SUM(amount) FROM agent_payments WHERE payment_type = 'balance_topup') -
            (SELECT SUM(purchase_amount) FROM user_bundles WHERE purchase_amount > 0)
        ) - SUM(ubi.money_balance),
        2
    ) AS global_mismatch
FROM userbillinfo ubi
WHERE ubi.money_balance != 0
   OR ubi.id IN (SELECT DISTINCT user_id FROM agent_payments);


-- --------------------------------------------------------
-- QUERY 2: Per-user - count topups vs count bundles purchased
-- If topup_count > bundle_count, bundles were missed (balance too high)
-- --------------------------------------------------------
SELECT * FROM (
    SELECT
        ubi.id AS user_id,
        ubi.username,
        ubi.money_balance AS current_balance,
        COALESCE(ap.topup_count, 0) AS topup_count,
        COALESCE(ap.total_topups, 0) AS total_topups,
        COALESCE(bun.bundle_count, 0) AS bundle_count,
        COALESCE(bun.total_bundle_cost, 0) AS total_bundle_cost,
        COALESCE(ap.topup_count, 0) - COALESCE(bun.bundle_count, 0) AS missed_bundles,
        ROUND(COALESCE(ap.total_topups, 0) - COALESCE(bun.total_bundle_cost, 0), 2) AS expected_balance,
        ROUND(
            (COALESCE(ap.total_topups, 0) - COALESCE(bun.total_bundle_cost, 0)) - ubi.money_balance,
            2
        ) AS mismatch,
        CASE
            WHEN ABS(
                (COALESCE(ap.total_topups, 0) - COALESCE(bun.total_bundle_cost, 0)) - ubi.money_balance
            ) < 0.01 THEN 'OK'
            ELSE 'MISMATCH'
        END AS status
    FROM userbillinfo ubi
    LEFT JOIN (
        SELECT user_id, COUNT(*) AS topup_count, SUM(amount) AS total_topups
        FROM agent_payments
        WHERE payment_type = 'balance_topup'
        GROUP BY user_id
    ) ap ON ap.user_id = ubi.id
    LEFT JOIN (
        SELECT user_id, COUNT(*) AS bundle_count, SUM(purchase_amount) AS total_bundle_cost
        FROM user_bundles
        WHERE purchase_amount > 0
        GROUP BY user_id
    ) bun ON bun.user_id = ubi.id
    WHERE ubi.money_balance != 0
       OR ap.total_topups IS NOT NULL
       OR bun.total_bundle_cost IS NOT NULL
) t
ORDER BY
    CASE WHEN status = 'MISMATCH' THEN 0 ELSE 1 END,
    missed_bundles DESC,
    username;


-- --------------------------------------------------------
-- QUERY 3: Payment trail - detect missed deductions between topups
-- Shows each topup and whether balance was deducted before the next topup
-- "NO DEDUCTION" = bundle wasn't bought, balance accumulated
-- --------------------------------------------------------
SELECT
    ap1.username,
    ap1.id AS payment_id,
    ap1.payment_date,
    ap1.amount AS topup_amount,
    ap1.user_balance_before,
    ap1.user_balance_after,
    ap2.id AS next_payment_id,
    ap2.payment_date AS next_payment_date,
    ap2.user_balance_before AS next_balance_before,
    CASE
        WHEN ap2.id IS NULL THEN 'LAST TOPUP'
        WHEN ap2.user_balance_before < ap1.user_balance_after THEN
            CONCAT('DEDUCTED ', ROUND(ap1.user_balance_after - ap2.user_balance_before, 2))
        WHEN ap2.user_balance_before = ap1.user_balance_after THEN
            'NO DEDUCTION'
        WHEN ap2.user_balance_before > ap1.user_balance_after THEN
            CONCAT('BALANCE INCREASED +', ROUND(ap2.user_balance_before - ap1.user_balance_after, 2))
        ELSE 'UNKNOWN'
    END AS deduction_status
FROM agent_payments ap1
LEFT JOIN agent_payments ap2
    ON ap2.user_id = ap1.user_id
    AND ap2.payment_type = 'balance_topup'
    AND ap2.id = (
        SELECT MIN(id)
        FROM agent_payments
        WHERE user_id = ap1.user_id
          AND payment_type = 'balance_topup'
          AND id > ap1.id
    )
WHERE ap1.payment_type = 'balance_topup'
ORDER BY ap1.username, ap1.payment_date;


-- --------------------------------------------------------
-- QUERY 4: Users with balance that should likely be 0
-- These users have money_balance > 0 but their total topups
-- roughly equal what they should have spent on bundles
-- (topup_count * typical_bundle_cost)
-- --------------------------------------------------------
SELECT * FROM (
    SELECT
        ubi.id AS user_id,
        ubi.username,
        ubi.money_balance AS current_balance,
        ubi.planName,
        COALESCE(ap.topup_count, 0) AS topup_count,
        COALESCE(ap.total_topups, 0) AS total_topups,
        COALESCE(bun.bundle_count, 0) AS bundles_recorded,
        COALESCE(bp.planCost, 0) AS plan_cost,
        -- Expected: each topup should have triggered a bundle purchase
        COALESCE(ap.topup_count, 0) * COALESCE(bp.planCost, 0) AS expected_total_spent,
        -- What should remain: total_topups - (topup_count * plan_cost)
        ROUND(COALESCE(ap.total_topups, 0) - (COALESCE(ap.topup_count, 0) * COALESCE(bp.planCost, 0)), 2) AS expected_remaining,
        -- Difference between actual and expected remaining
        ROUND(ubi.money_balance - (COALESCE(ap.total_topups, 0) - (COALESCE(ap.topup_count, 0) * COALESCE(bp.planCost, 0))), 2) AS excess_balance
    FROM userbillinfo ubi
    LEFT JOIN (
        SELECT user_id, COUNT(*) AS topup_count, SUM(amount) AS total_topups
        FROM agent_payments
        WHERE payment_type = 'balance_topup'
        GROUP BY user_id
    ) ap ON ap.user_id = ubi.id
    LEFT JOIN (
        SELECT user_id, COUNT(*) AS bundle_count
        FROM user_bundles
        WHERE purchase_amount > 0
        GROUP BY user_id
    ) bun ON bun.user_id = ubi.id
    LEFT JOIN billing_plans bp ON bp.planName = ubi.planName
    WHERE ubi.money_balance > 0
      AND ap.topup_count IS NOT NULL
) t
WHERE excess_balance > 0.01
ORDER BY excess_balance DESC;


-- --------------------------------------------------------
-- QUERY 5: Show ONLY mismatched users (quick audit)
-- --------------------------------------------------------
SELECT * FROM (
    SELECT
        ubi.id AS user_id,
        ubi.username,
        ubi.money_balance AS current_balance,
        COALESCE(ap.total_topups, 0) AS total_topups,
        COALESCE(bun.total_bundle_cost, 0) AS total_bundle_deductions,
        ROUND(COALESCE(ap.total_topups, 0) - COALESCE(bun.total_bundle_cost, 0), 2) AS expected_balance,
        ROUND(
            (COALESCE(ap.total_topups, 0) - COALESCE(bun.total_bundle_cost, 0)) - ubi.money_balance,
            2
        ) AS mismatch_amount
    FROM userbillinfo ubi
    LEFT JOIN (
        SELECT user_id, SUM(amount) AS total_topups
        FROM agent_payments
        WHERE payment_type = 'balance_topup'
        GROUP BY user_id
    ) ap ON ap.user_id = ubi.id
    LEFT JOIN (
        SELECT user_id, SUM(purchase_amount) AS total_bundle_cost
        FROM user_bundles
        WHERE purchase_amount > 0
        GROUP BY user_id
    ) bun ON bun.user_id = ubi.id
) t
WHERE ABS(mismatch_amount) >= 0.01
ORDER BY ABS(mismatch_amount) DESC;


-- --------------------------------------------------------
-- QUERY 6: Per-user detailed transaction ledger
-- Replace 'USERNAME_HERE' with actual username
-- --------------------------------------------------------
SELECT
    'TOPUP' AS type,
    ap.payment_date AS date,
    ap.amount,
    ap.user_balance_before AS balance_before,
    ap.user_balance_after AS balance_after,
    ap.notes,
    ap.created_by
FROM agent_payments ap
WHERE ap.username = 'USERNAME_HERE'
  AND ap.payment_type = 'balance_topup'

UNION ALL

SELECT
    'BUNDLE' AS type,
    ub.purchase_date AS date,
    -ub.purchase_amount AS amount,
    ub.balance_before,
    ub.balance_after,
    CONCAT(ub.plan_name, ' (', ub.status, ')') AS notes,
    ub.created_by
FROM user_bundles ub
WHERE ub.username = 'USERNAME_HERE'
  AND ub.purchase_amount > 0

ORDER BY date ASC;


-- --------------------------------------------------------
-- QUERY 7: Cross-check balance_history vs money_balance
-- Uses the transaction log (more accurate for recent data)
-- --------------------------------------------------------
SELECT * FROM (
    SELECT
        ubi.id AS user_id,
        ubi.username,
        ubi.money_balance AS current_balance,
        COALESCE(bh.total_credits, 0) AS total_credits,
        COALESCE(bh.total_debits, 0) AS total_debits,
        ROUND(COALESCE(bh.total_credits, 0) - COALESCE(bh.total_debits, 0), 2) AS expected_balance,
        ROUND(
            (COALESCE(bh.total_credits, 0) - COALESCE(bh.total_debits, 0)) - ubi.money_balance,
            2
        ) AS mismatch,
        CASE
            WHEN ABS(
                (COALESCE(bh.total_credits, 0) - COALESCE(bh.total_debits, 0)) - ubi.money_balance
            ) < 0.01 THEN 'OK'
            ELSE 'MISMATCH'
        END AS status
    FROM userbillinfo ubi
    LEFT JOIN (
        SELECT
            user_id,
            SUM(CASE WHEN amount > 0 THEN amount ELSE 0 END) AS total_credits,
            SUM(CASE WHEN amount < 0 THEN ABS(amount) ELSE 0 END) AS total_debits
        FROM user_balance_history
        GROUP BY user_id
    ) bh ON bh.user_id = ubi.id
    WHERE ubi.money_balance != 0
       OR bh.total_credits IS NOT NULL
) t
ORDER BY
    CASE WHEN status = 'MISMATCH' THEN 0 ELSE 1 END,
    ABS(mismatch) DESC,
    username;


